    public static String doGet(String url, Map<String, String> param) {

        // 创建Httpclient对象
        CloseableHttpClient httpclient = HttpClients.createDefault();

        String resultString = "";
        CloseableHttpResponse response = null;
        try {
            // 创建uri
            URIBuilder builder = new URIBuilder(url);
            if (param != null) {
                for (String key : param.keySet()) {
                    builder.addParameter(key, param.get(key));
                }
            }
            URI uri = builder.build();

            // 创建http GET请求
            HttpGet httpGet = new HttpGet(uri);

            // 执行请求
            response = httpclient.execute(httpGet);
            // 判断返回状态是否为200
            if (response.getStatusLine().getStatusCode() == 200) {
                resultString = EntityUtils.toString(response.getEntity(), "UTF-8");
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (response != null) {
                    response.close();
                }
                httpclient.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        return resultString;
    }

    public static String pubPost(String url, Map<String, String> param, MultipartFile file) {
        String resultString = "";
        try{
            HttpClient client=new DefaultHttpClient();
            HttpPost post = new HttpPost(url);
            ContentType contentType = ContentType.create("multipart/form-data", Consts.UTF_8);
            MultipartEntityBuilder builder = MultipartEntityBuilder.create().setMode(HttpMultipartMode.RFC6532);
            if (param != null) {
                for (String key : param.keySet()) {
                    builder.addPart(key, new StringBody(param.get(key),contentType));
                }
            }
            if (file != null){
                builder.addBinaryBody("file",file.getInputStream(),contentType,file.getOriginalFilename());
            }
            HttpEntity multipart = builder.build();
            post.setEntity(multipart);
            HttpResponse response = client.execute(post);
            HttpEntity responseEntity = response.getEntity();
            resultString = EntityUtils.toString(responseEntity, "UTF-8");
        }
        catch (Exception e){ e.printStackTrace(); }
        return resultString;
    }

